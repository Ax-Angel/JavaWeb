package modelo;

/**
 *
 * @author -Angel
 */
public class Producto {
    
    private int id;
    private String nombre;
    private String tipo;
    private int precio;
    
    public Producto(){
        id = 0;
        nombre = "";
        tipo = "";
        precio = 0;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public int getPrecio() {
        return precio;
    }

    public void setPrecio(int precio) {
        this.precio = precio;
    }
    
}
